    private void bTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTambahActionPerformed
        user usr = new user();
        usr.setUserName(tUserName.getText());
        usr.setUserPassword(tPassword.getText());
        usr.setUserEmail(tEmail.getText());
        if (cStatus.getSelectedItem()=="Active"){
            usr.setStatus(1);
        }else{
            usr.setStatus(0);
        }
        usr.setUserFullName(tFullName.getText());
        usr.tambahUser();
        load_table();
    }//GEN-LAST:event_bTambahActionPerformed

    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        // TODO add your handling code here:
        user usr = new user();
        DefaultTableModel model = new DefaultTableModel();
        int selectedRow = tblUser.getSelectedRow();
        String username = tblUser.getValueAt(selectedRow, 0).toString();
        if (selectedRow != -1){
            usr.setUserName(String.valueOf(username));
            usr.hapusUser();
            
            load_table();
        }else{
            JOptionPane.showMessageDialog(null, "Silahkan pilih 1 data yang ingin dihapus");
        }
    }//GEN-LAST:event_bHapusActionPerformed

